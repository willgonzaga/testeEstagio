    private void selecionaCliente(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_selecionaCliente
        // TODO add your handling code here:
        int row = clientesTable.getSelectedRow();
        if(row>=0) {
            int id = (int) clientesTable.getValueAt(row, 0);
            String nome = (String) clientesTable.getValueAt(row, 1);
            String cpf = (String) clientesTable.getValueAt(row, 2);
            String endereco = (String) clientesTable.getValueAt(row, 3);
            String telefone = (String) clientesTable.getValueAt(row, 4);
            String email = (String) clientesTable.getValueAt(row, 5);
            nomeText.setText(nome);
            cpfText.setText(cpf);
            enderecoText.setText(endereco);
            telefoneText.setText(telefone);
            emailText.setText(email);
            controller.clienteSelect(id, nome, cpf, endereco, telefone, email);
        }
    }//GEN-LAST:event_selecionaCliente

