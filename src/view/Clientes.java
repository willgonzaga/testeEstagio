    private void carregaTabela() {
        DefaultTableModel tabelaClientes = (DefaultTableModel) clientesTable.getModel();
        tabelaClientes.setNumRows(0);
        ResultSet rs;
        ConexaoDB connection;
        String sql;
        PreparedStatement stmt;
        
        clientesTable.getColumnModel().getColumn(0).setPreferredWidth(30);
        clientesTable.getColumnModel().getColumn(1).setPreferredWidth(30);
        clientesTable.getColumnModel().getColumn(2).setPreferredWidth(30);
        clientesTable.getColumnModel().getColumn(3).setPreferredWidth(30);
        clientesTable.getColumnModel().getColumn(4).setPreferredWidth(30);
        clientesTable.getColumnModel().getColumn(5).setPreferredWidth(30);
        
        try {
            connection = new ConexaoDB();
            sql = "select * from clientes order by id asc";
            stmt = connection.prepareStatement(sql);
            rs = stmt.executeQuery();
            
            while(rs.next()) {
                tabelaClientes.addRow(new Object[] {
                   rs.getInt(1),
                   rs.getString(2),
                   rs.getString(3),
                   rs.getString(4),
                   rs.getString(5),
                   rs.getString(6)
                });
            }
            stmt.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao carregar dados da tabela de clientes!");
        }
    }
    
    private void limpaCampos() {
        nomeText.setText("");
        cpfText.setText("");
        enderecoText.setText("");
        telefoneText.setText("");
        emailText.setText("");
    }
    
    private void editarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarButtonActionPerformed
        // TODO add your handling code here:     
        String nome = nomeText.getText();
        String cpf = cpfText.getText();
        String endereco = enderecoText.getText();
        String telefone = telefoneText.getText();
        String email = emailText.getText();
        try {
            if(nome.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nome do cliente não pode ser vazio!");
                nomeText.requestFocus();
            } else {
                controller.editarCliente(nome, cpf, endereco, telefone, email);
                carregaTabela();
                JOptionPane.showMessageDialog(null, "Cliente editado com sucesso!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_editarButtonActionPerformed

    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
        // TODO add your handling code here:
        
        String nome = nomeText.getText();
        String cpf = cpfText.getText();
        String endereco = enderecoText.getText();
        String telefone = telefoneText.getText();
        String email = emailText.getText();
        
        if(nome.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nome do cliente não pode ser vazio!");
            nomeText.requestFocus();
        } else {
            controller.cadastrarCliente(nome,cpf,endereco,telefone,email);
            carregaTabela();
            limpaCampos();
            JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso!");
        }
    }//GEN-LAST:event_salvarButtonActionPerformed

